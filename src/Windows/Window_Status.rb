#==============================================================================
# ** Window_Status
#------------------------------------------------------------------------------
#  This window displays full status specs on the status screen.
#==============================================================================

class Window_Status < Window_Base
  
  #//////////////////////////////////////////////////////////////////////////
  # * Properties
  #//////////////////////////////////////////////////////////////////////////
  
  #--------------------------------------------------------------------------
  # * Set the actor object
  #     actor : Actor object
  #--------------------------------------------------------------------------
  # SET
  def actor=(actor)
    @actor = actor
  end
  
  #--------------------------------------------------------------------------
  # * Alias refresh
  #--------------------------------------------------------------------------
  alias refresh_ebjb refresh unless $@
  def refresh
    if @actor != nil
      refresh_ebjb
    end
  end
  
end
